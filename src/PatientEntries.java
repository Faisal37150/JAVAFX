
public class PatientEntries {
	
	private String patId;
	private String patName;
	private String patAge;
	private String patDate;
	private String patGender;
	private String patAddress;
	private String patContact;
	private String patReg;
	private String patRoom;
	
	
	public PatientEntries(String patId, String patName, String patAge, String patDate, String patGender,
			String patAddress, String patContact, String patReg, String patRoom) {
		
		this.patId = patId;
		this.patName = patName;
		this.patAge = patAge;
		this.patDate = patDate;
		this.patGender = patGender;
		this.patAddress = patAddress;
		this.patContact = patContact;
		this.patReg = patReg;
		this.patRoom = patRoom;
	}

	public String getPatId() {
		return patId;
	}
	public void setPatId(String patId) {
		this.patId = patId;
	}
	public String getPatName() {
		return patName;
	}
	public void setPatName(String patName) {
		this.patName = patName;
	}
	public String getPatAge() {
		return patAge;
	}
	public void setPatAge(String patAge) {
		this.patAge = patAge;
	}
	public String getPatDate() {
		return patDate;
	}
	public void setPatDate(String patDate) {
		this.patDate = patDate;
	}
	public String getPatGender() {
		return patGender;
	}
	public void setPatGender(String patGender) {
		this.patGender = patGender;
	}
	public String getPatAddress() {
		return patAddress;
	}
	public void setPatAddress(String patAddress) {
		this.patAddress = patAddress;
	}
	public String getPatContact() {
		return patContact;
	}
	public void setPatContact(String patContact) {
		this.patContact = patContact;
	}
	public String getPatReg() {
		return patReg;
	}
	public void setPatReg(String patReg) {
		this.patReg = patReg;
	}
	public String getPatRoom() {
		return patRoom;
	}
	public void setPatRoom(String patRoom) {
		this.patRoom = patRoom;
	}
	
	

}
